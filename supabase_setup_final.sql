-- 4. HOLE TEMPLATES
INSERT INTO golf_course_holes (course_id, hole, par, distances)
SELECT 
    c.id, 
    h.num, 
    CASE WHEN h.num IN (3, 8, 12, 17) THEN 3 WHEN h.num IN (5, 7, 13, 18) THEN 5 ELSE 4 END as par,
    jsonb_strip_nulls(jsonb_build_object(
      'white', (CASE WHEN h.num IN (3, 8, 12, 17) THEN 160 WHEN h.num IN (5, 7, 13, 18) THEN 470 ELSE 350 END),
      'red', (CASE WHEN h.num IN (3, 8, 12, 17) THEN 110 WHEN h.num IN (5, 7, 13, 18) THEN 400 ELSE 290 END)
    )) as distances
FROM golf_courses c, generate_series(1, 18) h(num)
WHERE c.id NOT IN (SELECT DISTINCT course_id FROM golf_course_holes)
ON CONFLICT DO NOTHING;
